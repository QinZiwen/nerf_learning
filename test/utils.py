    # print('rays_d: \n', rays_d)
    # print("rays_d.shape: ", rays_d.shape)
    rays_o = np.broadcast_to(c2w[:3, -1], np.shape(rays_d))
    # p = c2w[:3, -1]
    # print("p: ", p)
    # print("rays_o.shape: ", rays_o.shape)
    return rays_o, rays_d

